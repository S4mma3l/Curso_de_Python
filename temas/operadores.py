a = 10
b = 5

# Operadores Aritméticos
print(a + b)  # Suma
print(a - b)  # Resta
print(a * b)  # Multiplicación  
print(a / b)  # División
print(a // b)  # División entera
print(a % b)  # Módulo
print(a ** b)  # Potencia

# Operadores de Asignación
x = 10
x += 5  # Ahora x es 15
print(x)

y = 20
y -= 2  # Ahora y es 18
print(y)

# Operadores de Comparación

edad = 18
print(edad == 18) # -> True
print(edad > 20)  # -> False
print(edad != 25) # -> True

# Operadores Lógicos

tiene_carnet = True
es_mayor_de_edad = True
puede_conducir = tiene_carnet and es_mayor_de_edad
print(puede_conducir) # -> True

esta_lloviendo = False
hace_frio = True
salir_con_paraguas = esta_lloviendo or hace_frio
print(salir_con_paraguas) # -> True

tiene_permiso = False
print(not tiene_permiso) # -> True


